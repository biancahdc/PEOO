    private void jrbCalcularSalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jrbCalcularSalActionPerformed
      if(jrbEstagiario.isSelected()){
            String nome = jtfNome.getText();
            String CPF = jtfCPF.getText();
            String salario = jtfSalario.getText();
            
            double sal = Double.parseDouble(salario);
            
            Estagiario e = new Estagiario(nome, CPF, sal);
            
            JOptionPane.showMessageDialog(this, e.toString());
      }
      
      
      if(jrbProfessor.isSelected()){
            String nome = jtfNome.getText();
            String CPF = jtfCPF.getText();
            String salario = jtfSalario.getText();
            
            double sal = Double.parseDouble(salario);
            
            Professor p = new Professor(nome, CPF, sal);
            
            JOptionPane.showMessageDialog(this, p.toString());
      }
      
      if(jrbDiretor.isSelected()){
            String nome = jtfNome.getText();
            String CPF = jtfCPF.getText();
            String salario = jtfSalario.getText();
            
            double sal = Double.parseDouble(salario);
            
            Diretor d = new Diretor(nome, CPF, sal);
            
            JOptionPane.showMessageDialog(this, d.toString());
      }
            
    }//GEN-LAST:event_jrbCalcularSalActionPerformed

